package thp.tp2.actividad3.ejercicio13;

public class Dvd {
	private String marca;
	private int capacidad;
	
	public Dvd(String marca, int capacidad) {
		this.marca = marca;
		this.capacidad = capacidad;
	}

	public String getMarca() {
		return marca;
	}

	public int getCapacidad() {
		return capacidad;
	}
	
}
